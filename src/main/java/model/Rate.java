package model;

/**
 *
 * @author AIK
 */
public class Rate {

    private String name;
    private Double value;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Double getValue() {
        return value;
    }

    public void setValue(Double value) {
        this.value = value;
    }

    public Rate(String name, double value) {
        super();
        this.name = name;
        this.value = value;
    }
}
