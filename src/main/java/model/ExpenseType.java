package model;

import com.fasterxml.jackson.annotation.JsonBackReference;
import java.io.Serializable;
import java.util.List;
import javax.persistence.Basic;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.NamedQueries;
import javax.persistence.NamedQuery;
import javax.persistence.OneToMany;
import javax.persistence.Table;
import javax.validation.constraints.NotNull;
import javax.validation.constraints.Size;

/**
 *
 * @author AIK
 */
@Entity
@Table(name = "expense_type")
@NamedQueries({
    @NamedQuery(name = "ExpenseType.findAll", query = "SELECT e FROM ExpenseType e")})
public class ExpenseType implements Serializable {

    private static final long serialVersionUID = 1L;
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    @Basic(optional = false)
    @Column(name = "id")
    private Long id;
    @Basic(optional = false)
    @NotNull
    @Size(min = 1, max = 50)
    @Column(name = "name")
    private String name;
    @OneToMany(mappedBy = "expenseType")
    private List<ExpenseLine> expenseLineList;

    public ExpenseType() {
    }

    public ExpenseType(Long id) {
        this.id = id;
    }

    public ExpenseType(Long id, String name) {
        this.id = id;
        this.name = name;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
    
    @JsonBackReference
    public List<ExpenseLine> getExpenseLineList() {
        return expenseLineList;
    }

    @JsonBackReference
    public void setExpenseLineList(List<ExpenseLine> expenseLineList) {
        this.expenseLineList = expenseLineList;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (id != null ? id.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof ExpenseType)) {
            return false;
        }
        ExpenseType other = (ExpenseType) object;
        if ((this.id == null && other.id != null) || (this.id != null && !this.id.equals(other.id))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "model.ExpenseType[ id=" + id + " ]";
    }
    
}
